package com.tcs.model;

import android.widget.TextView;

public class SlotViewHolder {
	private TextView slotTitle, slotContent, courseTitle, courseContent,
			facultyTitle, facultyContent, roomTitle, roomContent;

	public TextView getSlotTitle() {
		return slotTitle;
	}

	public void setSlotTitle(TextView slotTitle) {
		this.slotTitle = slotTitle;
	}

	public TextView getSlotContent() {
		return slotContent;
	}

	public void setSlotContent(TextView slotContent) {
		this.slotContent = slotContent;
	}

	public TextView getCourseTitle() {
		return courseTitle;
	}

	public void setCourseTitle(TextView courseTitle) {
		this.courseTitle = courseTitle;
	}

	public TextView getCourseContent() {
		return courseContent;
	}

	public void setCourseContent(TextView courseContent) {
		this.courseContent = courseContent;
	}

	public TextView getFacultyTitle() {
		return facultyTitle;
	}

	public void setFacultyTitle(TextView facultyTitle) {
		this.facultyTitle = facultyTitle;
	}

	public TextView getFacultyContent() {
		return facultyContent;
	}

	public void setFacultyContent(TextView facultyContent) {
		this.facultyContent = facultyContent;
	}

	public TextView getRoomTitle() {
		return roomTitle;
	}

	public void setRoomTitle(TextView roomTitle) {
		this.roomTitle = roomTitle;
	}

	public TextView getRoomContent() {
		return roomContent;
	}

	public void setRoomContent(TextView roomContent) {
		this.roomContent = roomContent;
	}

}
