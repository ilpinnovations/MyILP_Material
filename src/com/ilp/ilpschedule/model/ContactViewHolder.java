package com.ilp.ilpschedule.model;

import android.widget.TextView;

public class ContactViewHolder {
	private TextView textViewTitle, textViewNumber, textViewIcon;

	public TextView getTextViewTitle() {
		return textViewTitle;
	}

	public void setTextViewTitle(TextView textViewTitle) {
		this.textViewTitle = textViewTitle;
	}

	public TextView getTextViewNumber() {
		return textViewNumber;
	}

	public void setTextViewNumber(TextView textViewNumber) {
		this.textViewNumber = textViewNumber;
	}

	public TextView getTextViewIcon() {
		return textViewIcon;
	}

	public void setTextViewIcon(TextView textViewIcon) {
		this.textViewIcon = textViewIcon;
	}
}
